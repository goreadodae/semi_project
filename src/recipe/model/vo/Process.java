package recipe.model.vo;

public class Process {
	private int processNo;
	private int processOrder;
	private String processExplain;
	private String processPic;
	private String ingre;
	private String tools;
	private String fireLevel;
	private String tip;
	private int recipeNo;
	public Process() {

	}
	public int getProcessNo() {
		return processNo;
	}
	public void setProcessNo(int processNo) {
		this.processNo = processNo;
	}
	public int getProcessOrder() {
		return processOrder;
	}
	public void setProcessOrder(int processOrder) {
		this.processOrder = processOrder;
	}
	public String getProcessExplain() {
		return processExplain;
	}
	public void setProcessExplain(String processExplain) {
		this.processExplain = processExplain;
	}
	public String getProcessPic() {
		return processPic;
	}
	public void setProcessPic(String processPic) {
		this.processPic = processPic;
	}
	public String getIngre() {
		return ingre;
	}
	public void setIngre(String ingre) {
		this.ingre = ingre;
	}
	public String getTools() {
		return tools;
	}
	public void setTools(String tools) {
		this.tools = tools;
	}
	public String getFireLevel() {
		return fireLevel;
	}
	public void setFireLevel(String fireLevel) {
		this.fireLevel = fireLevel;
	}
	public String getTip() {
		return tip;
	}
	public void setTip(String tip) {
		this.tip = tip;
	}
	public int getRecipeNo() {
		return recipeNo;
	}
	public void setRecipeNo(int recipeNo) {
		this.recipeNo = recipeNo;
	}
	
}
